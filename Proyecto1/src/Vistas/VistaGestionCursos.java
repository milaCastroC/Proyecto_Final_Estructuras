    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (validarCamposTexto()) {
            try {
                String codigo = txtCodigo.getText();
                String nombre = txtNombre.getText();
                //SelecciÃ³n del programa
                String programaSeleccionado = cbPrograma.getSelectedItem().toString();
                String idPrograma = programaSeleccionado.split("-")[1].trim();
                Programa programa = controladorGestionCursos.buscarPrograma(idPrograma);
                String jornada;
                if (cbJornada.getSelectedItem().equals("Diurna")) {
                    jornada = Curso.DIURNA;
                } else {
                    jornada = Curso.NOCTURNA;
                }
                String docenteSeleccionado = cbDocente.getSelectedItem().toString();
                String idDocente = docenteSeleccionado.split("-")[1].trim();
                Docente docente = controladorGestionCursos.buscarDocente(idDocente);
                Curso curso = new Curso(nombre, docente, codigo, programa, jornada);
                controladorGestionCursos.validarCurso(curso);
                controladorGestionCursos.agregarCurso(curso);
                JOptionPane.showMessageDialog(null, "Curso guardado con Ã©xito");
                llenarTablaCursos();
                limpiarCampos();
                llenarCb();
            } catch (CursoYaExistenteException | ProgramaNoEncontradoException | UsuarioInexistenteException | UsuarioNoCoincideException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codigo = txtCodigo.getText();
        if (codigo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Se necesita ingresar el cÃ³digo del curso que desea buscar");
        } else {
            try {
                Curso cursoEncontrado = controladorGestionCursos.buscarCurso(codigo);
                txtCodigo.setText(cursoEncontrado.getCodigoCurso());
                txtNombre.setText(cursoEncontrado.getNombre());
                cbPrograma.setSelectedItem(cursoEncontrado.getPrograma().getNombre() + "-" + cursoEncontrado.getPrograma().getCodigo());
                cbJornada.setSelectedItem(cursoEncontrado.getJornada());
                cbDocente.setSelectedItem(cursoEncontrado.getDocente().getNombre() + "-" + cursoEncontrado.getDocente().getId());
            } catch (CursoInexistenteException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

