    public boolean validarCamposTexto() {
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();

        if (nombre.isEmpty() || codigo.isEmpty()) {
            return false;
        }
        return true;
    }

    public void limpiarCampos() {
        txtCodigo.setText("");
        txtNombre.setText("");
        cbPrograma.setSelectedItem(0);
        cbJornada.setSelectedItem(0);
        cbDocente.setSelectedItem(0);
    }

    public void limpiarCamposHorario() {
        cbDias.setSelectedItem(0);
        txtHoraInicio.setText("00:00");
        txtHoraFin.setText("00:00");
    }

    public DayOfWeek obtenerDia() throws DiaInvalidoException {
        String dia = String.valueOf(cbDias.getSelectedItem());
        switch (dia) {
            case "Lunes":
                return MONDAY;
            case "Martes":
                return TUESDAY;
            case "MiÃ©rcoles":
                return WEDNESDAY;
            case "Jueves":
                return THURSDAY;
            case "Viernes":
                return FRIDAY;
        }
        throw new DiaInvalidoException();
    }

    
