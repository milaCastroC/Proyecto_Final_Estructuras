    public void llenarTablaCursos() {
        IList<Curso> cursos = controladorGestionCursos.getCursos();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "Codigo", "Nombre", "Programa", "Jornada", "Periodo", "Docente","Estado"
        });
        for (int i = 0; i < cursos.size(); i++) {
            Curso curso = cursos.get(i);
                model.addRow(new Object[]{
                    curso.getCodigoCurso(),
                    curso.getNombre(),
                    curso.getPrograma().getNombre(),
                    curso.getJornada(),
                    curso.getPeriodo(),
                    curso.getDocente().getNombre(),
                    curso.getEstado()
                });
        }
        tablaCursos.setModel(model);
    }

    public void llenarTablaHorarios(Curso curso) {
        IList<Horario> horarios = controladorGestionCursos.getListaHorario(curso);
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "Horario"
        });
        for (int i = 0; i < horarios.size(); i++) {
            model.addRow(new Object[]{
                horarios.get(i)
            });
        }
        tablaHorarios.setModel(model);
    }

    public void llenarCb() {
        IList<Docente> docentes = controladorGestionCursos.getListaDocentes();
        IList<Programa> programas = controladorGestionCursos.getListaProgramas();
        IList<Curso> cursos = controladorGestionCursos.getCursos();
        cbDocente.removeAllItems();
        for (int i = 0; i < docentes.size(); i++) {
            Docente docente = docentes.get(i);
            cbDocente.addItem(docente.getNombre() + "-" + docente.getId());
        }
        cbPrograma.removeAllItems();
        for (int i = 0; i < programas.size(); i++) {
            Programa programa = programas.get(i);
            cbPrograma.addItem(programa.getNombre() + "-" + programa.getCodigo());
        }
        cbCurso.removeAllItems();
        if (!cursos.isEmpty()) {
            for (int i = 0; i < cursos.size(); i++) {
                Curso curso = cursos.get(i);
                   cbCurso.addItem(cursos.get(i).getNombre() + "-" + cursos.get(i).getCodigoCurso()); 
            }
        }
    }

