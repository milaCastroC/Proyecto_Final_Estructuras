    private void btnAgregarHorarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarHorarioActionPerformed
        try {
            String cursoSeleccionado = (String) cbCurso.getSelectedItem();
            if (cursoSeleccionado != null) {
                String codigoCurso = cursoSeleccionado.split("-")[1].trim();
                Curso curso = controladorGestionCursos.buscarCurso(codigoCurso);
                DayOfWeek dia = obtenerDia();
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm");
                LocalTime horaInicio = LocalTime.parse(txtHoraInicio.getText(), formatter);
                LocalTime horaFin = LocalTime.parse(txtHoraFin.getText(), formatter);
                Horario horario = new Horario(horaInicio, horaFin, dia);
                controladorGestionCursos.agregarHorario(curso, horario);
                llenarTablaHorarios(curso);
                limpiarCamposHorario();
                JOptionPane.showMessageDialog(null, "Horario agregado con Ã©xito");
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione un curso para agregar un horario");
            }
        } catch (HoraInvalidaException | DocenteNoDisponibleException | DiaInvalidoException | FueraDeJornadaException | CursoInexistenteException | HorarioInvalidoException | EstudianteNoDisponibleException | CursoFinalizadoException  ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }catch(DateTimeParseException ex){
            JOptionPane.showMessageDialog(null, "Hora invÃ¡lida");
        } 
    }//GEN-LAST:event_btnAgregarHorarioActionPerformed

    private void btnEliminarHorarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarHorarioActionPerformed
        int fila = tablaHorarios.getSelectedRow();
            if (fila != -1) {
                try {
                    String seleccion = (String) cbCurso.getSelectedItem();
                    String codigoCurso = seleccion.split("-")[1].trim();
                    Curso curso = controladorGestionCursos.buscarCurso(codigoCurso);
                    Horario horario = (Horario) tablaHorarios.getValueAt(fila, 0);
                    controladorGestionCursos.eliminarHorario(curso, horario);
                    llenarTablaHorarios(curso);
                    JOptionPane.showMessageDialog(null, "Horario eliminado con Ã©xito");
                } catch (CursoInexistenteException | CursoFinalizadoException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione en la tabla el horario que desea eliminar");
            }
    }//GEN-LAST:event_btnEliminarHorarioActionPerformed

