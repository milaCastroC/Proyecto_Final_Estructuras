    public void llenarTablaEstudiantes(){
        IList<Persona>usuarios = controladorGestionEstudiante.getUsuarios();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "ID", "Nombre", "Edad","Fecha de nacimiento", "Programa", "Semestre", "Telefono", "Nombre de Usuario", "Contraseña"
        });
        for(int i = 0; i < usuarios.size(); i++){
            Persona usuario = usuarios.get(i);
            if(usuario instanceof Estudiante){
                Estudiante estudiante = (Estudiante)usuario;
                model.addRow(new Object[]{
                    estudiante.getId(),
                    estudiante.getNombre(),
                    estudiante.calcularEdad(),
                    estudiante.getFechaNacimiento(),
                    estudiante.getPrograma().getNombre(),
                    estudiante.getSemestre(),
                    estudiante.getTelefono(),
                    estudiante.getNombreUsuario(),
                    estudiante.getContrasena(),
                });
            }
        }
        tablaEstudiantes.setModel(model);
    }
    
    private void actualizarComboBoxPrograma(){
        IList<Programa>programas = controladorGestionEstudiante.getProgramas();
        for(int i = 0; i < programas.size(); i++){
            Programa programa = programas.get(i);
            cboPrograma.addItem(programa.getNombre()+"-"+programa.getCodigo());
            for (int j = 1; j <= programa.getSemestres(); j++){
                cboSemestre.addItem(String.valueOf(j));
            }
        }
    }
