    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(validarCamposTexto()){
            try{
                String nombre = txtNombre.getText();
                String id = txtId.getText();
                String telefono = txtTelefono.getText();
                //Selección del programa
                String programaSeleccionado = cboPrograma.getSelectedItem().toString();
                String idPrograma = programaSeleccionado.split("-")[1].trim();
                Programa programa = controladorGestionEstudiante.buscarPrograma(idPrograma);
                String semestre =  cboSemestre.getSelectedItem().toString();
                LocalDate fechaNacimiento = convertirDateALocalDate(calendarioFechaNacimiento.getDate());
                String nombreUsuario = txtNombreUsuario.getText();
                String contrasena = txtContrasena.getText();
                Estudiante estudiante = new Estudiante(programa, semestre, nombre, id, telefono, fechaNacimiento, nombreUsuario, contrasena,Usuario.ESTUDIANTE);
                controladorGestionEstudiante.validarEstudiante(id, nombreUsuario);
                controladorGestionEstudiante.calcularEdad(estudiante);
                controladorGestionEstudiante.agregarEstudiante(estudiante);
                JOptionPane.showMessageDialog(null, "Estudiante guardado con éxito");
               llenarTablaEstudiantes();
               limpiarCampos();
            }catch(UsuarioYaRegistradoException | EdadInvalidaException | NombreUsuarioRepetidoException |ProgramaNoEncontradoException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } 
        }else{
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String ID = txtId.getText();
        if(ID.isEmpty()){
            JOptionPane.showMessageDialog(null, "Se necesita ingresar la identificación del estudiante que desea buscar");
        }else{
            try{
                Estudiante estudianteEncontrado = (Estudiante) controladorGestionEstudiante.buscarEstudiante(ID);
                txtId.setText(estudianteEncontrado.getId());
                txtNombre.setText(estudianteEncontrado.getNombre());
                txtTelefono.setText(estudianteEncontrado.getTelefono());
                cboPrograma.setSelectedItem(estudianteEncontrado.getPrograma().getNombre() + "-" + estudianteEncontrado.getPrograma().getCodigo());
                cboSemestre.setSelectedItem(estudianteEncontrado.getSemestre());
                 /*Date.from convierte un Instant en un Date.
                administrativoEncontrado.getFechaNacimiento()obtiene la fecha de nacimiento que está en formato LocalDate
                atStartOfDay(ZoneId.systemDefault()) convierte la fecha de LocalDate a LocalDateTime
                toInstant() convierte el localDteTime en un Instante que representa un instante específico*/
                calendarioFechaNacimiento.setDate(Date.from(estudianteEncontrado.getFechaNacimiento().atStartOfDay(ZoneId.systemDefault()).toInstant()));
                txtNombreUsuario.setText(estudianteEncontrado.getNombreUsuario());
                txtContrasena.setText(estudianteEncontrado.getContrasena());
            }catch(UsuarioInexistenteException | UsuarioNoCoincideException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

