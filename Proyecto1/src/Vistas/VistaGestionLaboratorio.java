    public void llenarTablaMantenimientos(){
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "Fecha", "Motivo","Estado"
        });
        IList<Mantenimiento>mantenimientos = controladorGestLab.getMantenimientos();
        for(int i = 0; i < mantenimientos.size(); i++){
            Mantenimiento mantenimiento = mantenimientos.get(i);
            model.addRow(new Object[]{
                mantenimiento.getDia(),
                mantenimiento.getMotivo(),
                mantenimiento.getEstado()
            });
        }
        tablaMantenimientos.setModel(model);
    }

