    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try{
            int cantidadPuestosModificado = (int) spCantPuestos.getValue();
            int cantidadPuestosActual = controladorGestLab.getCantidadPuestos();
            if (cantidadPuestosModificado != cantidadPuestosActual) {
                controladorGestLab.actualizarCantidadPuestos(cantidadPuestosModificado);
                JOptionPane.showMessageDialog(null, "Cantidad de puestos modificada");
                VistaPuestos vistaGesLab = new VistaPuestos(laboratorio, usuario);
                vistaGesLab.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "La cantidad de puestos no es válida");
        }
        }catch(CantidadPuestosInvalidaException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnProgramarMantenimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProgramarMantenimientoActionPerformed
        Date fecha = calendario.getDate();
        String motivo = txtMotivo.getText();
        if(fecha == null && motivo.isEmpty()){
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
        }else{
            LocalDate fechaMantenimiento = convertirDateALocalDate(fecha);
            try{
                Mantenimiento mantenimiento = new Mantenimiento(motivo, fechaMantenimiento);
                controladorGestLab.agregarMantenimiento(mantenimiento);
                JOptionPane.showMessageDialog(null, "Mantenimiento agendado con éxito");
                llenarTablaMantenimientos();
                limpiarCamposTexto();
            }catch(MantenimientoInvalidoException | MantenimientoExistenteException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnProgramarMantenimientoActionPerformed

    private void tablaMantenimientosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMantenimientosMouseClicked
        try {
            int fila = tablaMantenimientos.getSelectedRow();
            LocalDate fechaMantenimiento = (LocalDate) tablaMantenimientos.getValueAt(fila, 0);
            Mantenimiento mantenimiento = controladorGestLab.buscarMantenimiento(fechaMantenimiento);
            Date fecha = Date.from(mantenimiento.getDia().atStartOfDay(ZoneId.systemDefault()).toInstant());
            calendario.setDate(fecha);
            txtMotivo.setText(mantenimiento.getMotivo());
        }catch(MantenimientoInexistenteException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_tablaMantenimientosMouseClicked

