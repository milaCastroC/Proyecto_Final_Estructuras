    private void btnCambiarContrasenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarContrasenaActionPerformed
        String nuevaContrasena = String.valueOf(txtNuevaContrasena.getPassword());
        String contrasenaConfirmada = String.valueOf(txtConfirmarContrasena.getPassword());
        
        try {
             if (!nuevaContrasena.equals(contrasenaConfirmada)) {
                    throw new ContrasenaIncorrectaException();
              }
            Usuario usuario = controladorCambioContrasena.buscarUsuario(nombreUsuario);
            if(nuevaContrasena.equals(usuario.getContrasena())){
                throw new ContrasenaRepetidaException();
            }
            controladorCambioContrasena.cambiarContrasena(nombreUsuario, nuevaContrasena);
            
            switch (usuario.getRol()) {
                case Usuario.ADMINGENERAL-> {
                    new VistaAdministradorGeneral(vistaLogin).setVisible(true);
                    this.dispose();
                }
                case Usuario.ESTUDIANTE -> {
                    new VistaEstudiante((Estudiante) usuario).setVisible(true);
                    this.dispose();
                }
                case Usuario.DOCENTE -> {
                    new VistaPrincipalDocente((Docente) usuario).setVisible(true);
                    this.dispose();
                }
                case Usuario.ADMINLAB -> {
                    new VistaLaboratorio(usuario).setVisible(true);
                    this.dispose();
                }
                case Usuario.ADMINISTRATIVO ->{
                    new VistaAdministrativo().setVisible(true);
                    this.dispose();
                }
            }
        } catch (ContrasenaIncorrectaException | UsuarioInexistenteException | ContrasenaRepetidaException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnCambiarContrasenaActionPerformed

