    public boolean validarCamposTextoProgramas(){
        String nombre = txtNombrePrograma.getText();
        String codigo = txtCodigoPrograma.getText();
        String facultad = txtFacultadPrograma.getText();
        int semestres = Integer.parseInt(spSemestresPrograma.getValue().toString());
        if(nombre.isEmpty() || codigo.isEmpty() || facultad.isEmpty()|| semestres <= 0){
            return false;
        }
        return true;
    }
    
    public void limpiarCamposProgramas(){
        txtNombrePrograma.setText("");
        txtCodigoPrograma.setText("");
        txtFacultadPrograma.setText("");
        spSemestresPrograma.setValue(0);
    }
