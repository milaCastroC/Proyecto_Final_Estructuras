    public void llenarTablaProgramas() {
        IList<Programa> programas = controladorPrograma.getPrograma();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "Nombre", "Codigo", "Facultad", "Semestres"
        });
        for (int i = 0; i < programas.size(); i++) {
            model.addRow(new Object[]{
                programas.get(i).getNombre(),
                programas.get(i).getCodigo(),
                programas.get(i).getFacultad(),
                programas.get(i).getSemestres()
            });
            
        }
        tablaProgramas.setModel(model);
    }

