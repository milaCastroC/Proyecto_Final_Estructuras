    private void btnAgregarProgramaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProgramaActionPerformed
        if (validarCamposTextoProgramas()) {
            try {
                String nombre = txtNombrePrograma.getText();
                String codigo = txtCodigoPrograma.getText();
                String facultad = txtFacultadPrograma.getText();
                int semestres = Integer.parseInt(spSemestresPrograma.getValue().toString());
                Programa programa = new Programa(nombre, codigo, facultad, semestres);
                controladorPrograma.validarPrograma(codigo);
                controladorPrograma.agregarPrograma(programa);
                JOptionPane.showMessageDialog(null, "Programa guardado con exito");
                llenarTablaProgramas();
                limpiarCamposProgramas();
            }catch(ProgramaYaRegistradoException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos de texto o verifique que estén dentro de rangos establecidos");
        }
    }//GEN-LAST:event_btnAgregarProgramaActionPerformed

    private void btnBuscarProgramaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProgramaActionPerformed
       String codigo = txtCodigoPrograma.getText();
        if(codigo.isEmpty()){
            JOptionPane.showMessageDialog(null, "Se necesita ingresar el código del programa que desea buscar");
        }else{
            try{
                Programa programaEncontrado = controladorPrograma.buscarPrograma(codigo);
                txtNombrePrograma.setText(programaEncontrado.getNombre());
                txtFacultadPrograma.setText(programaEncontrado.getFacultad());
                spSemestresPrograma.setValue(programaEncontrado.getSemestres());
            }catch(ProgramaNoEncontradoException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } 
    }//GEN-LAST:event_btnBuscarProgramaActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String codigo = txtCodigoPrograma.getText();
        if(codigo.isEmpty()){
            JOptionPane.showMessageDialog(null, "Se necesita ingresar el código del perro que desea editar");
        }else{
            if(validarCamposTextoProgramas()){
                try{
                    String nombre = txtNombrePrograma.getText();
                    String facultad = txtFacultadPrograma.getText();
                    int semestres = Integer.parseInt(spSemestresPrograma.getValue().toString());
                    Programa programaActualizado = new Programa(nombre, codigo, facultad, semestres);
                    controladorPrograma.editarPrograma(codigo, programaActualizado);
                    JOptionPane.showMessageDialog(null, "Programa editado con exito");
                    llenarTablaProgramas();
                    limpiarCamposProgramas();
                }catch(ProgramaNoEncontradoException ex){
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String codigo = txtCodigoPrograma.getText();
        if(codigo.isEmpty()){
            JOptionPane.showMessageDialog(null, "Se necesita ingresar el código del programa que desea eliminar");
        }else{
            try{
                controladorPrograma.eliminarPrograma(codigo);
                JOptionPane.showMessageDialog(null, "Programa eliminado con éxito");
                llenarTablaProgramas();
                limpiarCamposProgramas();
            }catch(ProgramaNoEncontradoException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

