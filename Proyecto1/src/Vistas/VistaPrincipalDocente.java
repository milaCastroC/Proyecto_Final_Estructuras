    public void llenarTablaHorario() {
        DefaultTableModel model = new DefaultTableModel();
        try {
            String[] lunes = controladorDocente.getHorario(DayOfWeek.MONDAY);
            model.addColumn("Lunes", lunes);

            String[] martes = controladorDocente.getHorario(DayOfWeek.TUESDAY);
            model.addColumn("Martes", martes);

            String[] miercoles = controladorDocente.getHorario(DayOfWeek.WEDNESDAY);
            model.addColumn("Miercoles", miercoles);

            String[] jueves = controladorDocente.getHorario(DayOfWeek.THURSDAY);
            model.addColumn("Jueves", jueves);

            String[] viernes = controladorDocente.getHorario(DayOfWeek.FRIDAY);
            model.addColumn("Viernes", viernes);
        } catch (UsuarioNoCoincideException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

        tablaHorario.setModel(model);
    }

