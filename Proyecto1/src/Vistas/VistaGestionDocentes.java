    public void llenarTablaDocentes(){
        IList<Persona>usuarios = controladorGestionDocente.getUsuarios();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "ID","Nombre","Edad","Fecha de Nacimiento", "Profesion","Telefono","Nombre de usuario","Contrase침a"
        });
        for(int i = 0; i < usuarios.size(); i ++){
            Persona usuario = usuarios.get(i);
            if(usuario instanceof Docente){
                Docente docente = (Docente)usuario;
                model.addRow(new Object[]{
                docente.getId(),
                docente.getNombre(),
                docente.calcularEdad(),
                docente.getFechaNacimiento(),
                docente.getProfesion(),
                docente.getTelefono(),
                docente.getNombreUsuario(),
                docente.getContrasena(),
            });
            }
            
        }
        tablaDocentes.setModel(model);
    }

