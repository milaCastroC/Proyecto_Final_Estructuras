    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       if(validarCamposTexto()){
           try{
               String nombre = txtNombre.getText();
               String id = txtId.getText();
               String telefono = txtTelefono.getText();
               String profesion = txtProfesion.getText();
               LocalDate fechaNacimiento = convertirDateALocalDate(calendarioFechaNacimiento.getDate());
               String nombreUsuario = txtNombreUsuario.getText();
               String contrasena = txtContrasena.getText();
               Docente docente = new Docente(profesion, nombre, id, telefono, fechaNacimiento, nombreUsuario, contrasena, Usuario.DOCENTE);
               controladorGestionDocente.validarDocente(id, nombreUsuario);
               controladorGestionDocente.CalcularEdad(docente);
               controladorGestionDocente.agregarDocente(docente);
               JOptionPane.showMessageDialog(null, "Docente guardado con 칠xito");
               llenarTablaDocentes();
               limpiarCampos();
           }catch(UsuarioYaRegistradoException | EdadInvalidaException | NombreUsuarioRepetidoException  ex){
               JOptionPane.showMessageDialog(null, ex.getMessage());
           }
       }else{
           JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
       }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String ID = txtId.getText();
        if(ID.isEmpty()){
            JOptionPane.showMessageDialog(null, "Se necesita ingresar la identificaci칩n del docente que desea buscar");
        }else{
            try{
                Docente docenteEncontrado = controladorGestionDocente.buscarDocente(ID);
                txtId.setText(docenteEncontrado.getId());
                txtNombre.setText(docenteEncontrado.getNombre());
                txtProfesion.setText(docenteEncontrado.getProfesion());
                txtTelefono.setText(docenteEncontrado.getTelefono());
                 /*Date.from convierte un Instant en un Date.
                administrativoEncontrado.getFechaNacimiento()obtiene la fecha de nacimiento que est치 en formato LocalDate
                atStartOfDay(ZoneId.systemDefault()) convierte la fecha de LocalDate a LocalDateTime
                toInstant() convierte el localDteTime en un Instante que representa un instante espec칤fico*/
                calendarioFechaNacimiento.setDate(Date.from(docenteEncontrado.getFechaNacimiento().atStartOfDay(ZoneId.systemDefault()).toInstant()));
                txtNombreUsuario.setText(docenteEncontrado.getNombreUsuario());
                txtContrasena.setText(docenteEncontrado.getContrasena());
            }catch(UsuarioInexistenteException | UsuarioNoCoincideException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String ID = txtId.getText();
        if(ID.isEmpty()){
            JOptionPane.showMessageDialog(null, "Se necesita ingresar la identificaci칩n del docente que desea editar");
        }else{
            if(validarCamposTexto()){
                try{
                    String nombre = txtNombre.getText();
                    String telefono = txtTelefono.getText();
                    String profesion = txtProfesion.getText();
                    LocalDate fechaNacimiento = convertirDateALocalDate(calendarioFechaNacimiento.getDate());
                    String nombreUsuario = txtNombreUsuario.getText();
                    String contrasena = txtContrasena.getText();
                    Docente docenteActualizado = new Docente(profesion, nombre, ID, telefono, fechaNacimiento, nombreUsuario, contrasena, Usuario.DOCENTE);
                    controladorGestionDocente.editarDocente(ID, docenteActualizado);
                    JOptionPane.showMessageDialog(null, "Docente editado con 칠xito");
                    llenarTablaDocentes();
                    limpiarCampos();
                }catch(UsuarioInexistenteException ex){
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }else{
                JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

