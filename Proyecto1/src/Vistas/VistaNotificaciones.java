    public void llenarTablaNotificaciones(){
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
           "NÂ°", "Fecha", "Mensaje"
        });
        
        IList<Notificacion>notificaciones = controladorNotificaciones.getListaNotificaciones();
        for(int i = 0; i < notificaciones.size(); i++){
            Notificacion notificacion = notificaciones.get(i);
            notificacion.getMensaje();
            model.addRow(new Object[]{
                notificacion.getIdentificador(),
                notificacion.getFormatoFecha(),
                notificacion.getMensaje()
            });
        }
        tablaNotificaciones.setModel(model);
        tablaNotificaciones.getColumnModel().getColumn(0).setPreferredWidth(25);
        tablaNotificaciones.getColumnModel().getColumn(1).setPreferredWidth(75);
        tablaNotificaciones.getColumnModel().getColumn(2).setPreferredWidth(710);
    }

